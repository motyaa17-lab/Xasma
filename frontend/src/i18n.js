/** Valid UI language codes. */
const LANG_CODES = new Set(["en", "ru", "uk"]);

export function normalizeLang(lang) {
  const s = String(lang || "").trim();
  return LANG_CODES.has(s) ? s : "en";
}

/** BCP 47 locale for date/time formatting. */
export function localeForLang(lang) {
  const n = normalizeLang(lang);
  if (n === "ru") return "ru-RU";
  if (n === "uk") return "uk-UA";
  return "en-US";
}

/** Localized name of the selected UI language (for settings summary row). */
export function currentLanguageLabel(lang, t) {
  const n = normalizeLang(lang);
  if (n === "ru") return t("langRussianName");
  if (n === "uk") return t("langUkrainianName");
  return t("langEnglishName");
}

export function t(lang, key) {
  const table = translations[normalizeLang(lang)] || translations.en;
  return table[key] || translations.en[key] || key;
}

export function tf(lang, key, vars) {
  let s = t(lang, key);
  const v = vars || {};
  Object.keys(v).forEach((k) => {
    s = s.replaceAll(`{${k}}`, String(v[k]));
  });
  return s;
}

/** Maps login/register failures to a clear UX message (uses `t` from current language). */
export function formatAuthError(err, t) {
  const status = err?.status;
  if (err?.name === "ApiError" && typeof status === "number") {
    if (status === 0 || status >= 500) {
      return t("authServerUnavailable");
    }
    if (status === 403 && String(err.message || "").toLowerCase().includes("banned")) {
      return t("authBanned");
    }
    if (status === 401) {
      return t("authInvalidCredentials");
    }
    return err.message || t("authGenericError");
  }
  return err?.message || t("authGenericError");
}

